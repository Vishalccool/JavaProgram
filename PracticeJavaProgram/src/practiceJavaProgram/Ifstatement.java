package practiceJavaProgram;

public class Ifstatement  // single condition
{
   public static void main(String[] args) 
  {
	
    int marks = 100;
    if (marks>=40)
    	
   {
	System.out.println("Student is Passed");
   }
	
// when if statement we use the condition(<,>) 	
// In this program we are checking the marks condition is passed 40 above to 100 
// 40<100 for ex-60 is passed --> this statement is printing
// 40<100 for ex-38 is not passed --> this statement is not printing
  
  }  
   
}

